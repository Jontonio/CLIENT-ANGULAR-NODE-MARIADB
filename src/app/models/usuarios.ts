
export class Usuario {

    id:number
    nombre:string
    email:string
    estado:boolean
    createdAt:any
    updatedAt:any

    constructor(){
        this.id = this.id;
        this.nombre = this.nombre;
        this.email = this.email;
        this.estado = this.estado;
        this.createdAt = this.createdAt;
        this.updatedAt = this.updatedAt;
    }
}